//Módulos são formas de dividir em partes do seu código.

function soma(a, b) {
    return a + b
}
function subtracao(a, b) {
    return a - b
}
function multiplicacao(a, b) {
    return a * b
}
function divisao(a, b) {
  return a / b;
}

console.log(soma(35, 85));
console.log(subtracao(35, 85));
console.log(multiplicacao(35, 85));
console.log(divisao(35, 85));
